#include <iostream>
using namespace std;

int main(){
    
    // Primitive/built In data types --> int, float, double, char, bool, wchar_t
    int i = 10;
    float f = 10.5;
    double d = 10.5;
    char c = 'a';
    bool b = true;
    wchar_t w = L'a';

    cout<< "printing primitive data types"<<endl;
    cout << i << endl;
    cout << f << endl;
    cout << d << endl;
    cout << c << endl;
    cout << b << endl;
    cout << w << endl;
    return 0;
}